// Options are the sqlc plugin options for the store wrapper generator.
type Options struct {
	// OutputFileName is the generated Go file name.
	OutputFileName string `json:"output_file_name"`
	// SourcePackage is the package containing the source query types.
	SourcePackage string `json:"source_package"`

	// PackageName is the package clause used in generated code.
	PackageName string `json:"package"`
	// InternalImportPath imports sqlc output when wrappers use a separate package.
	InternalImportPath string `json:"internal_import_path"`
	// InternalImportAlias overrides the imported sqlc package qualifier.
	InternalImportAlias string `json:"internal_import_alias"`
	// InterfaceName is the sqlc-generated interface the store must implement.
	InterfaceName string `json:"interface"`
	// TypeName is the combined read-write wrapper type name.
	TypeName string `json:"type"`
	// TargetTypeName is the sqlc-generated query type name.
	TargetTypeName string `json:"target_type"`
	// ConstructorName is the combined wrapper constructor name.
	ConstructorName string `json:"constructor"`
	// TargetConstructorName is the sqlc-generated query constructor name.
	TargetConstructorName string `json:"target_constructor"`
	// ReceiverName is the receiver identifier used by generated methods.
	ReceiverName string `json:"receiver"`
	// SkipWithTx requests omission of transaction methods and is currently unsupported.
	SkipWithTx bool `json:"skip_with_tx"`
	// IgnoreMirrorError makes generated writes discard non-NoRows mirror errors.
	IgnoreMirrorError bool `json:"ignore_mirror_error"`
	// ReadInterfaceName is the generated read-only interface name.
	ReadInterfaceName string `json:"read_interface"`
	// WriteInterfaceName is the generated write interface name.
	WriteInterfaceName string `json:"write_interface"`
	// StoreInterfaceName is the generated combined query interface name.
	StoreInterfaceName string `json:"store_interface"`
	// ReadTypeName is the generated read-only wrapper type name.
	ReadTypeName string `json:"read_type"`
	// WriteTypeName is the generated primary-capable wrapper type name.
	WriteTypeName string `json:"write_type"`
	// ReadConstructorName is the read-only wrapper constructor name.
	ReadConstructorName string `json:"read_constructor"`
	// WriteConstructorName is the primary-capable wrapper constructor name.
	WriteConstructorName string `json:"write_constructor"`
	// RuntimeImportPath is the import path for the pgmesh runtime package.
	RuntimeImportPath string `json:"runtime_import_path"`
	// NodeConstructorName is the generated database node constructor name.
	NodeConstructorName string `json:"node_constructor"`
	// ResolverInterfaceName is the generated shard resolver interface name.
	ResolverInterfaceName string `json:"resolver_interface"`
	// ShardedTypeName is the generated routed facade type name.
	ShardedTypeName string `json:"sharded_type"`
	// ShardedConstructor is the routed facade constructor name.
	ShardedConstructor string `json:"sharded_constructor"`

	// SQLPackage selects the sqlc database driver; only pgx/v5 is supported.
	SQLPackage string `json:"sql_package"`
	// QueryParameterLimit matches sqlc's threshold for generating parameter structs.
	QueryParameterLimit *int32 `json:"query_parameter_limit"`
	// EmitParamsStructPointers matches sqlc's parameter-struct pointer setting.
	EmitParamsStructPointers bool `json:"emit_params_struct_pointers"`
	// EmitResultStructPointers matches sqlc's result-struct pointer setting.
	EmitResultStructPointers bool `json:"emit_result_struct_pointers"`
	// EmitPointersForNullTypes matches sqlc's nullable-type pointer setting.
	EmitPointersForNullTypes bool `json:"emit_pointers_for_null_types"`
	// EmitPointersForNullEnumType overrides pointer emission for nullable enums.
	EmitPointersForNullEnumType *bool `json:"emit_pointers_for_null_enum_types"`
	// EmitExactTableNames preserves table names when deriving generated Go types.
	EmitExactTableNames bool `json:"emit_exact_table_names"`
	// Rename maps SQL identifiers to generated Go identifiers.
	Rename map[string]string `json:"rename"`
	// Overrides applies sqlc-compatible database and column type overrides.
	Overrides []override `json:"overrides"`
}

// Generate implements sqlc's CodegenService.Generate hook.
func Generate(_ context.Context, req *plugin.GenerateRequest) (*plugin.GenerateResponse, error) {
	opts, err := parseOptions(req)
	if err != nil {
		return nil, err
	}

	queries, imports, err := querySpecs(req, opts)
	if err != nil {
		return nil, err
	}

	content, err := generateWrapper(opts, queries, imports)
	if err != nil {
		return nil, err
	}
	return &plugin.GenerateResponse{
		Files: []*plugin.File{{
			Name:     opts.OutputFileName,
			Contents: content,
		}},
	}, nil
}

func parseOptions(req *plugin.GenerateRequest) (*Options, error) {
	if req.GetSettings().GetEngine() != "postgresql" {
		return nil, fmt.Errorf(
			"engine %q is unsupported; pgmesh v1 requires postgresql",
			req.GetSettings().GetEngine(),
		)
	}
	opts := &Options{} //nolint:exhaustruct // Populated from JSON and normalized with defaults below.
	if len(req.GetPluginOptions()) > 0 {
		if err := json.Unmarshal(req.GetPluginOptions(), opts); err != nil {
			return nil, fmt.Errorf("unmarshal plugin options: %w", err)
		}
	}

	if opts.OutputFileName == "" {
		opts.OutputFileName = defaultOutputFileName
	}
	if opts.PackageName == "" {
		opts.PackageName = outputPackageName(req)
	}
	if opts.SourcePackage == "" {
		opts.SourcePackage = opts.PackageName
	}
	if opts.SQLPackage == "" {
		opts.SQLPackage = defaultSQLPackage
	}
	if opts.QueryParameterLimit == nil {
		limit := int32(1)
		opts.QueryParameterLimit = &limit
	}
	if opts.TypeName == "" {
		opts.TypeName = storeTypeName(opts)
	}
	if opts.TargetTypeName == "" {
		opts.TargetTypeName = defaultTargetType
	}
	if opts.TargetConstructorName == "" {
		opts.TargetConstructorName = defaultTargetNew
	}
	if opts.ConstructorName == "" {
		opts.ConstructorName = "New" + opts.TypeName
	}
	if opts.ReceiverName == "" {
		opts.ReceiverName = defaultReceiverName
	}
	if opts.ReadInterfaceName == "" {
		opts.ReadInterfaceName = defaultReadInterface
	}
	if opts.WriteInterfaceName == "" {
		opts.WriteInterfaceName = defaultWriteInterface
	}
	if opts.StoreInterfaceName == "" {
		opts.StoreInterfaceName = defaultStoreInterface
	}
	if opts.ReadTypeName == "" {
		opts.ReadTypeName = defaultReadType
	}
	if opts.WriteTypeName == "" {
		opts.WriteTypeName = defaultWriteType
	}
	if opts.ReadConstructorName == "" {
		opts.ReadConstructorName = "New" + opts.ReadTypeName
	}
	if opts.WriteConstructorName == "" {
		opts.WriteConstructorName = "New" + opts.WriteTypeName
	}
	if opts.RuntimeImportPath == "" {
		opts.RuntimeImportPath = defaultRuntimePackage
	}
	if opts.NodeConstructorName == "" {
		opts.NodeConstructorName = defaultNodeNew
	}
	if opts.ResolverInterfaceName == "" {
		opts.ResolverInterfaceName = defaultResolver
	}
	if opts.ShardedTypeName == "" {
		opts.ShardedTypeName = defaultShardedType
	}
	if opts.ShardedConstructor == "" {
		opts.ShardedConstructor = defaultShardedNew
	}
	if *opts.QueryParameterLimit < 0 {
		return nil, errors.New("query parameter limit must not be negative")
	}
	if opts.SQLPackage != string(sqlDriverPGXV5) {
		return nil, fmt.Errorf("sql_package %q is unsupported; pgmesh v1 requires pgx/v5", opts.SQLPackage)
	}
	if opts.SkipWithTx {
		return nil, errors.New("skip_with_tx is unsupported; pgmesh requires generated pgx/v5 transaction support")
	}

	for idx := range opts.Overrides {
		if err := opts.Overrides[idx].parse(req); err != nil {
			return nil, fmt.Errorf("parse override %d: %w", idx, err)
		}
	}

	return opts, nil
}

func outputPackageName(req *plugin.GenerateRequest) string {
	if codegen := req.GetSettings().GetCodegen(); codegen != nil && codegen.GetOut() != "" {
		name := filepath.Base(codegen.GetOut())
		if validRouteIdentifier(name) && !goKeywords[name] {
			return name
		}
	}
	return defaultPackageName
}

type importSpec struct {
	name string
	path string
}

type importSet struct {
	byPath map[string]importSpec
}

func newImportSet() *importSet {
	return &importSet{byPath: map[string]importSpec{}}
}

func (s *importSet) add(path string) {
	s.addNamed("", path)
}

func (s *importSet) addNamed(name, path string) {
	if path == "" {
		return
	}
	s.byPath[path] = importSpec{name: name, path: path}
}

func (s *importSet) addForType(typ string) {
	for qualifier, path := range knownSelectorImports {
		if typeUsesQualifier(typ, qualifier) {
			s.add(path)
		}
	}
}

func (s *importSet) sorted() []importSpec {
	imports := make([]importSpec, 0, len(s.byPath))
	for _, imp := range s.byPath {
		imports = append(imports, imp)
	}
	sort.Slice(imports, func(i, j int) bool {
		if imports[i].path == imports[j].path {
			return imports[i].name < imports[j].name
		}
		return imports[i].path < imports[j].path
	})
	return imports
}

var knownSelectorImports = map[string]string{
	"driver":   "database/sql/driver",
	"context":  "context",
	"json":     "encoding/json",
	"net":      "net",
	"netip":    "net/netip",
	"sql":      "database/sql",
	"time":     "time",
	"uuid":     "github.com/google/uuid",
	"pgconn":   "github.com/jackc/pgx/v5/pgconn",
	"pgtype":   "github.com/jackc/pgx/v5/pgtype",
	"pgvector": "github.com/pgvector/pgvector-go",
	"pqtype":   "github.com/sqlc-dev/pqtype",
}

func typeUsesQualifier(typ, qualifier string) bool {
	return strings.Contains(typ, qualifier+".") || strings.Contains(typ, "*"+qualifier+".") ||
		strings.Contains(typ, "[]"+qualifier+".")
}

type typeResolver struct {
	req     *plugin.GenerateRequest
	opts    *Options
	imports *importSet
}

func querySpecs(req *plugin.GenerateRequest, opts *Options) ([]generatedQuery, *importSet, error) {
	resolver := &typeResolver{
		req:     req,
		opts:    opts,
		imports: newImportSet(),
	}
	structs := buildStructs(req, opts, resolver)
	queries, err := buildQueries(req, opts, resolver, structs)
	if err != nil {
		return nil, nil, err
	}
	return queries, resolver.imports, nil
}

func generateWrapper(opts *Options, queries []generatedQuery, imports *importSet) ([]byte, error) {
	routes, err := collectShardRoutes(queries)
	if err != nil {
		return nil, err
	}
	if opts.InternalImportPath != "" {
		imports.addNamed(importAlias(opts.InternalImportAlias), opts.InternalImportPath)
	}
	imports.addNamed("pgmesh", opts.RuntimeImportPath)
	if !opts.SkipWithTx {
		imports.add(defaultPGXPackage)
	}
	if !opts.IgnoreMirrorError {
		imports.add(defaultDatabaseSQL)
		imports.add(defaultErrorsPackage)
	}

	var out bytes.Buffer
	fmt.Fprintf(&out, "%s\n\n", generatedHeader)
	fmt.Fprintf(&out, "package %s\n\n", opts.PackageName)
	writeImports(&out, imports.sorted())

	writeSplitInterface(&out, opts.ReadInterfaceName, queries, queryKindRead)
	writeSplitInterface(&out, opts.WriteInterfaceName, queries, queryKindWrite)
	fmt.Fprintf(&out, "// %s combines the generated read and write query interfaces.\n", opts.StoreInterfaceName)
	fmt.Fprintf(&out, "type %s interface {\n", opts.StoreInterfaceName)
	fmt.Fprintf(&out, "\t%s\n", opts.ReadInterfaceName)
	fmt.Fprintf(&out, "\t%s\n", opts.WriteInterfaceName)
	out.WriteString("}\n\n")

	fmt.Fprintf(&out, "var _ %s = (*%s)(nil)\n", opts.ReadInterfaceName, opts.ReadTypeName)
	fmt.Fprintf(&out, "var _ %s = (*%s)(nil)\n", opts.WriteInterfaceName, opts.WriteTypeName)
	fmt.Fprintf(&out, "var _ %s = (*%s)(nil)\n", opts.StoreInterfaceName, opts.TypeName)
	fmt.Fprintf(&out, "var _ %s = (*%s)(nil)\n\n", interfaceName(opts), opts.TypeName)

	writeReadQueries(&out, opts, queries)
	writeWriteQueries(&out, opts, queries)
	writeStoreQueries(&out, opts)
	writeNodeConstructor(&out, opts)
	if len(routes) > 0 {
		writeShardedQueries(&out, opts, queries, routes)
	}

	formatted, err := format.Source(out.Bytes())
	if err != nil {
		return nil, fmt.Errorf("format generated wrapper: %w", err)
	}
	return formatted, nil
}

func writeReadQueries(out *bytes.Buffer, opts *Options, queries []generatedQuery) {
	fmt.Fprintf(out, "// %s exposes read-only generated queries.\n", opts.ReadTypeName)
	fmt.Fprintf(out, "type %s struct {\n", opts.ReadTypeName)
	fmt.Fprintf(out, "\tmain *%s\n", targetName(opts, opts.TargetTypeName))
	out.WriteString("}\n\n")

	fmt.Fprintf(out, "// %s creates a read-only query wrapper.\n", opts.ReadConstructorName)
	fmt.Fprintf(out, "func %s(database %s) *%s {\n", opts.ReadConstructorName, targetName(opts, "DBTX"), opts.ReadTypeName)
	fmt.Fprintf(
		out,
		"\treturn %s(%s(database))\n",
		privateConstructorName(opts.ReadConstructorName),
		targetName(opts, opts.TargetConstructorName),
	)
	out.WriteString("}\n\n")

	fmt.Fprintf(
		out,
		"func %s(q *%s) *%s {\n",
		privateConstructorName(opts.ReadConstructorName),
		targetName(opts, opts.TargetTypeName),
		opts.ReadTypeName,
	)
	fmt.Fprintf(out, "\treturn &%s{main: q}\n", opts.ReadTypeName)
	out.WriteString("}\n\n")

	if !opts.SkipWithTx {
		out.WriteString("// WithTx returns a read wrapper that executes queries through tx.\n")
		fmt.Fprintf(out, "func (%s *%s) WithTx(tx pgx.Tx) *%s {\n", opts.ReceiverName, opts.ReadTypeName, opts.ReadTypeName)
		fmt.Fprintf(out, "\treturn %s(%s.main.WithTx(tx))\n", privateConstructorName(opts.ReadConstructorName), opts.ReceiverName)
		out.WriteString("}\n\n")
	}

	writeQueryMethods(out, opts, opts.ReadTypeName, queries, queryKindRead, false)
}

func writeWriteQueries(out *bytes.Buffer, opts *Options, queries []generatedQuery) {
	fmt.Fprintf(out, "// %s exposes primary-capable generated queries.\n", opts.WriteTypeName)
	fmt.Fprintf(out, "type %s struct {\n", opts.WriteTypeName)
	fmt.Fprintf(out, "\tmain *%s\n", targetName(opts, opts.TargetTypeName))
	fmt.Fprintf(out, "\tmirrors []*%s\n", targetName(opts, opts.TargetTypeName))
	out.WriteString("}\n\n")

	fmt.Fprintf(out, "// %s creates a primary-capable query wrapper.\n", opts.WriteConstructorName)
	fmt.Fprintf(out, "func %s(database %s) *%s {\n", opts.WriteConstructorName, targetName(opts, "DBTX"), opts.WriteTypeName)
	fmt.Fprintf(
		out,
		"\treturn %s(%s(database))\n",
		privateConstructorName(opts.WriteConstructorName),
		targetName(opts, opts.TargetConstructorName),
	)
	out.WriteString("}\n\n")

	fmt.Fprintf(
		out,
		"func %s(q *%s, mirrors ...*%s) *%s {\n",
		privateConstructorName(opts.WriteConstructorName),
		targetName(opts, opts.TargetTypeName),
		targetName(opts, opts.TargetTypeName),
		opts.WriteTypeName,
	)
	fmt.Fprintf(out, "\treturn &%s{main: q, mirrors: mirrors}\n", opts.WriteTypeName)
	out.WriteString("}\n\n")

	if !opts.SkipWithTx {
		out.WriteString("// WithTx returns a write wrapper that executes queries through tx.\n")
		fmt.Fprintf(out, "func (%s *%s) WithTx(tx pgx.Tx) *%s {\n", opts.ReceiverName, opts.WriteTypeName, opts.WriteTypeName)
		fmt.Fprintf(out, "\treturn %s(%s.main.WithTx(tx))\n", privateConstructorName(opts.WriteConstructorName), opts.ReceiverName)
		out.WriteString("}\n\n")
	}

	out.WriteString("// WithMirrors returns a copy that also writes to the supplied mirrors.\n")
	fmt.Fprintf(
		out,
		"func (%s *%s) WithMirrors(qs ...*%s) *%s {\n",
		opts.ReceiverName,
		opts.WriteTypeName,
		opts.WriteTypeName,
		opts.WriteTypeName,
	)
	fmt.Fprintf(out, "\tvar mirrors []*%s\n", targetName(opts, opts.TargetTypeName))
	fmt.Fprintf(out, "\tmirrors = append(mirrors, %s.mirrors...)\n", opts.ReceiverName)
	out.WriteString("\tfor _, mirror := range qs {\n")
	out.WriteString("\t\tif mirror == nil {\n")
	out.WriteString("\t\t\tcontinue\n")
	out.WriteString("\t\t}\n")
	out.WriteString("\t\tmirrors = append(mirrors, mirror.main)\n")
	out.WriteString("\t\tmirrors = append(mirrors, mirror.mirrors...)\n")
	out.WriteString("\t}\n")
	fmt.Fprintf(out, "\treturn %s(%s.main, mirrors...)\n", privateConstructorName(opts.WriteConstructorName), opts.ReceiverName)
	out.WriteString("}\n\n")

	fmt.Fprintf(
		out,
		"func (%s *%s) mirror(fn func(*%s) error) error {\n",
		opts.ReceiverName,
		opts.WriteTypeName,
		targetName(opts, opts.TargetTypeName),
	)
	fmt.Fprintf(out, "\tfor _, mirror := range %s.mirrors {\n", opts.ReceiverName)
	out.WriteString("\t\tif err := fn(mirror); err != nil {\n")
	if opts.IgnoreMirrorError {
		out.WriteString("\t\t\tcontinue\n")
	} else {
		out.WriteString("\t\t\tif errors.Is(err, sql.ErrNoRows) {\n")
		out.WriteString("\t\t\t\tcontinue\n")
		out.WriteString("\t\t\t}\n")
		out.WriteString("\t\t\treturn err\n")
	}
	out.WriteString("\t\t}\n")
	out.WriteString("\t}\n")
	out.WriteString("\treturn nil\n")
	out.WriteString("}\n\n")

	writeQueryMethods(out, opts, opts.WriteTypeName, queries, queryKindWrite, true)
}

func writeStoreQueries(out *bytes.Buffer, opts *Options) {
	fmt.Fprintf(out, "// %s combines read-only and primary-capable generated queries.\n", opts.TypeName)
	fmt.Fprintf(out, "type %s struct {\n", opts.TypeName)
	fmt.Fprintf(out, "\t*%s\n", opts.ReadTypeName)
	fmt.Fprintf(out, "\t*%s\n", opts.WriteTypeName)
	out.WriteString("}\n\n")

	fmt.Fprintf(out, "// %s creates a combined query wrapper.\n", opts.ConstructorName)
	fmt.Fprintf(out, "func %s(database %s) *%s {\n", opts.ConstructorName, targetName(opts, "DBTX"), opts.TypeName)
	fmt.Fprintf(
		out,
		"\treturn %s(%s(database))\n",
		privateConstructorName(opts.ConstructorName),
		targetName(opts, opts.TargetConstructorName),
	)
	out.WriteString("}\n\n")

	fmt.Fprintf(
		out,
		"func %s(q *%s, mirrors ...*%s) *%s {\n",
		privateConstructorName(opts.ConstructorName),
		targetName(opts, opts.TargetTypeName),
		targetName(opts, opts.TargetTypeName),
		opts.TypeName,
	)
	fmt.Fprintf(out, "\treturn &%s{\n", opts.TypeName)
	fmt.Fprintf(out, "\t\t%s:  %s(q),\n", opts.ReadTypeName, privateConstructorName(opts.ReadConstructorName))
	fmt.Fprintf(out, "\t\t%s: %s(q, mirrors...),\n", opts.WriteTypeName, privateConstructorName(opts.WriteConstructorName))
	out.WriteString("\t}\n")
	out.WriteString("}\n\n")

	if !opts.SkipWithTx {
		out.WriteString("// WithTx returns a store wrapper that executes queries through tx.\n")
		fmt.Fprintf(out, "func (%s *%s) WithTx(tx pgx.Tx) *%s {\n", opts.ReceiverName, opts.TypeName, opts.TypeName)
		fmt.Fprintf(
			out,
			"\treturn %s(%s.%s.main.WithTx(tx))\n",
			privateConstructorName(opts.ConstructorName),
			opts.ReceiverName,
			opts.WriteTypeName,
		)
		out.WriteString("}\n\n")
	}

	out.WriteString("// WithMirrors returns a copy that also writes to the supplied mirrors.\n")
	fmt.Fprintf(out, "func (%s *%s) WithMirrors(qs ...*%s) *%s {\n", opts.ReceiverName, opts.TypeName, opts.TypeName, opts.TypeName)
	fmt.Fprintf(out, "\tvar mirrors []*%s\n", targetName(opts, opts.TargetTypeName))
	fmt.Fprintf(out, "\tmirrors = append(mirrors, %s.%s.mirrors...)\n", opts.ReceiverName, opts.WriteTypeName)
	out.WriteString("\tfor _, mirror := range qs {\n")
	fmt.Fprintf(out, "\t\tif mirror == nil || mirror.%s == nil {\n", opts.WriteTypeName)
	out.WriteString("\t\t\tcontinue\n")
	out.WriteString("\t\t}\n")
	fmt.Fprintf(out, "\t\tmirrors = append(mirrors, mirror.%s.main)\n", opts.WriteTypeName)
	fmt.Fprintf(out, "\t\tmirrors = append(mirrors, mirror.%s.mirrors...)\n", opts.WriteTypeName)
	out.WriteString("\t}\n")
	fmt.Fprintf(
		out,
		"\treturn %s(%s.%s.main, mirrors...)\n",
		privateConstructorName(opts.ConstructorName),
		opts.ReceiverName,
		opts.WriteTypeName,
	)
	out.WriteString("}\n\n")
}

func writeNodeConstructor(out *bytes.Buffer, opts *Options) {
	fmt.Fprintf(out, "// %s creates a pgmesh node backed by database.\n", opts.NodeConstructorName)
	fmt.Fprintf(
		out,
		"func %s(database %s) pgmesh.Node[*%s, *%s] {\n",
		opts.NodeConstructorName,
		targetName(opts, "DBTX"),
		opts.ReadTypeName,
		opts.TypeName,
	)
	fmt.Fprintf(out, "\tqueries := %s(database)\n", targetName(opts, opts.TargetConstructorName))
	fmt.Fprintf(
		out,
		"\treturn pgmesh.NewNode(%s(queries), %s(queries))\n",
		privateConstructorName(opts.ReadConstructorName),
		privateConstructorName(opts.ConstructorName),
	)
	out.WriteString("}\n\n")
}

func writeShardedQueries(
	out *bytes.Buffer,
	opts *Options,
	queries []generatedQuery,
	routes []shardRoute,
) {
	fmt.Fprintf(out, "// %s resolves generated query parameters to shard keys.\n", opts.ResolverInterfaceName)
	fmt.Fprintf(out, "type %s[SK any] interface {\n", opts.ResolverInterfaceName)
	for _, route := range routes {
		fmt.Fprintf(out, "\t// %s resolves the %q shard route.\n", route.methodName, route.name)
		fmt.Fprintf(out, "\t%s(%s) SK\n", route.methodName, routeOperandsSignature(route.operands))
	}
	out.WriteString("}\n\n")

	out.WriteString("type routeOptions struct {\n")
	out.WriteString("\tprimary bool\n")
	out.WriteString("\ttx pgx.Tx\n")
	out.WriteString("}\n\n")
	out.WriteString("// RouteOption customizes routing for one generated query call.\n")
	out.WriteString("type RouteOption func(*routeOptions)\n\n")
	out.WriteString("// ReadFromPrimary routes a read query to the shard's primary.\n")
	out.WriteString("func ReadFromPrimary() RouteOption {\n")
	out.WriteString("\treturn func(options *routeOptions) { options.primary = true }\n")
	out.WriteString("}\n\n")
	out.WriteString("// WithTx routes a query through tx and suppresses write mirrors.\n")
	out.WriteString("func WithTx(tx pgx.Tx) RouteOption {\n")
	out.WriteString("\treturn func(options *routeOptions) { options.tx = tx }\n")
	out.WriteString("}\n\n")
	out.WriteString("func applyRouteOptions(options ...RouteOption) routeOptions {\n")
	out.WriteString("\tvar result routeOptions\n")
	out.WriteString("\tfor _, option := range options {\n")
	out.WriteString("\t\tif option != nil { option(&result) }\n")
	out.WriteString("\t}\n")
	out.WriteString("\treturn result\n")
	out.WriteString("}\n\n")

	fmt.Fprintf(out, "// %s routes generated queries through a pgmesh mesh.\n", opts.ShardedTypeName)
	fmt.Fprintf(out, "type %s[SK any] struct {\n", opts.ShardedTypeName)
	fmt.Fprintf(out, "\tmesh *pgmesh.Mesh[*%s, *%s, SK]\n", opts.ReadTypeName, opts.TypeName)
	fmt.Fprintf(out, "\tresolver %s[SK]\n", opts.ResolverInterfaceName)
	out.WriteString("}\n\n")
	fmt.Fprintf(out, "// %s creates a routed query facade.\n", opts.ShardedConstructor)
	fmt.Fprintf(
		out,
		"func %s[SK any](mesh *pgmesh.Mesh[*%s, *%s, SK], resolver %s[SK]) *%s[SK] {\n",
		opts.ShardedConstructor,
		opts.ReadTypeName,
		opts.TypeName,
		opts.ResolverInterfaceName,
		opts.ShardedTypeName,
	)
	fmt.Fprintf(out, "\treturn &%s[SK]{mesh: mesh, resolver: resolver}\n", opts.ShardedTypeName)
	out.WriteString("}\n\n")

	for index := range queries {
		query := &queries[index]
		if query.route == nil {
			continue
		}
		writeShardedQueryMethod(out, opts, query)
	}
}

func routeOperandsSignature(operands []routeOperand) string {
	parts := make([]string, 0, len(operands))
	for _, operand := range operands {
		parts = append(parts, operand.name+" "+operand.typ)
	}
	return strings.Join(parts, ", ")
}

func writeShardedQueryMethod(out *bytes.Buffer, opts *Options, query *generatedQuery) {
	params := paramsSignature(query.params)
	if params != "" {
		params += ", "
	}
	params += "routeOptions ...RouteOption"
	fmt.Fprintf(out, "// %s executes the generated query on its resolved shard.\n", query.methodName)
	fmt.Fprintf(
		out,
		"func (%s *%s[SK]) %s(%s)%s {\n",
		opts.ReceiverName,
		opts.ShardedTypeName,
		query.methodName,
		params,
		resultsSignature(query.results),
	)
	queryKind := "pgmesh.QueryKindRead"
	if query.kind == queryKindWrite {
		queryKind = "pgmesh.QueryKindWrite"
	}
	fmt.Fprintf(
		out,
		"\tctx, queryTrace := %s.mesh.StartQueryTrace(ctx, %q, %s)\n",
		opts.ReceiverName,
		query.methodName,
		queryKind,
	)
	out.WriteString("\tvar queryErr error\n")
	out.WriteString("\tdefer func() { queryTrace.End(queryErr) }()\n")
	routeArgs := make([]string, 0, len(query.route.operands))
	for _, operand := range query.route.operands {
		routeArgs = append(routeArgs, operand.expression)
	}
	fmt.Fprintf(
		out,
		"\tshardKey := %s.resolver.%s(%s)\n",
		opts.ReceiverName,
		query.route.methodName,
		strings.Join(routeArgs, ", "),
	)
	fmt.Fprintf(out, "\tshard, err := %s.mesh.Shard(shardKey)\n", opts.ReceiverName)
	out.WriteString("\tif err != nil {\n")
	out.WriteString("\t\tqueryErr = err\n")
	writeGeneratedErrorReturn(out, query.results, "err", "\t\t")
	out.WriteString("\t}\n")
	out.WriteString("\toptions := applyRouteOptions(routeOptions...)\n")
	args := callArguments(query.params)
	if query.kind == queryKindRead {
		out.WriteString("\tif options.tx != nil {\n")
		out.WriteString("\t\tqueryTrace.SetRoute(shard.VShardIndex(), shard.Name(), pgmesh.RouteModeTransaction, 0)\n")
		writeTracedQueryCall(
			out,
			query,
			fmt.Sprintf("shard.Write().WithTx(options.tx).%s(%s)", query.methodName, args),
			"\t\t",
		)
		out.WriteString("\t}\n")
		out.WriteString("\tif options.primary {\n")
		out.WriteString("\t\tqueryTrace.SetRoute(shard.VShardIndex(), shard.Name(), pgmesh.RouteModePrimary, 0)\n")
		writeTracedQueryCall(
			out,
			query,
			fmt.Sprintf("shard.Write().%s(%s)", query.methodName, args),
			"\t\t",
		)
		out.WriteString("\t}\n")
		out.WriteString("\tqueryTrace.SetRoute(shard.VShardIndex(), shard.Name(), pgmesh.RouteModeRead, 0)\n")
		writeTracedQueryCall(
			out,
			query,
			fmt.Sprintf("shard.Read().%s(%s)", query.methodName, args),
			"\t",
		)
	} else {
		out.WriteString("\ttarget := shard.Write()\n")
		out.WriteString("\tmode := pgmesh.RouteModePrimary\n")
		out.WriteString("\twriteMirrorCount := shard.WriteMirrorCount()\n")
		out.WriteString("\tif options.tx != nil {\n")
		out.WriteString("\t\ttarget = target.WithTx(options.tx)\n")
		out.WriteString("\t\tmode = pgmesh.RouteModeTransaction\n")
		out.WriteString("\t\twriteMirrorCount = 0\n")
		out.WriteString("\t}\n")
		out.WriteString("\tqueryTrace.SetRoute(shard.VShardIndex(), shard.Name(), mode, writeMirrorCount)\n")
		writeTracedQueryCall(
			out,
			query,
			fmt.Sprintf("target.%s(%s)", query.methodName, args),
			"\t",
		)
	}
	out.WriteString("}\n\n")
}

func writeTracedQueryCall(
	out *bytes.Buffer,
	query *generatedQuery,
	call string,
	indent string,
) {
	if !lastResultIsError(query.results) {
		fmt.Fprintf(out, "%sreturn %s\n", indent, call)
		return
	}

	nonErrorResults := query.results[:len(query.results)-1]
	if len(nonErrorResults) == 0 {
		fmt.Fprintf(out, "%squeryErr = %s\n", indent, call)
		out.WriteString(indent + "return queryErr\n")
		return
	}

	resultVars := resultVariables(len(nonErrorResults))
	for index, result := range nonErrorResults {
		fmt.Fprintf(out, "%svar %s %s\n", indent, resultVars[index], result)
	}
	fmt.Fprintf(
		out,
		"%s%s, queryErr = %s\n",
		indent,
		strings.Join(resultVars, ", "),
		call,
	)
	fmt.Fprintf(out, "%sreturn %s, queryErr\n", indent, strings.Join(resultVars, ", "))
}

func writeGeneratedErrorReturn(out *bytes.Buffer, results []string, errExpression, indent string) {
	nonError := results[:len(results)-1]
	for index, result := range nonError {
		fmt.Fprintf(out, "%svar zero%d %s\n", indent, index, result)
	}
	fmt.Fprint(out, indent+"return ")
	for index := range nonError {
		if index > 0 {
			out.WriteString(", ")
		}
		fmt.Fprintf(out, "zero%d", index)
	}
	if len(nonError) > 0 {
		out.WriteString(", ")
	}
	fmt.Fprintln(out, errExpression)
}

func writeQueryMethods(
	out *bytes.Buffer,
	opts *Options,
	receiverType string,
	queries []generatedQuery,
	kind queryKind,
	mirror bool,
) {
	for idx := range queries {
		query := &queries[idx]
		if query.kind != kind {
			continue
		}
		fmt.Fprintf(out, "// %s executes the generated %s query.\n", query.methodName, query.methodName)
		fmt.Fprintf(out, "func (%s *%s) %s(%s)%s {\n",
			opts.ReceiverName,
			receiverType,
			query.methodName,
			paramsSignature(query.params),
			resultsSignature(query.results),
		)
		writeQueryMethodBody(out, opts, query, mirror)
		out.WriteString("}\n\n")
	}
}

func writeQueryMethodBody(out *bytes.Buffer, opts *Options, query *generatedQuery, mirror bool) {
	args := callArguments(query.params)
	if !lastResultIsError(query.results) {
		fmt.Fprintf(out, "\treturn %s.main.%s(%s)\n", opts.ReceiverName, query.methodName, args)
		return
	}

	nonErrorResults := query.results[:len(query.results)-1]
	if len(nonErrorResults) == 0 {
		fmt.Fprintf(out, "\tif err := %s.main.%s(%s); err != nil {\n", opts.ReceiverName, query.methodName, args)
		out.WriteString("\t\treturn err\n")
		out.WriteString("\t}\n")
		if !mirror {
			out.WriteString("\treturn nil\n")
			return
		}
		if opts.IgnoreMirrorError {
			fmt.Fprintf(
				out,
				"\t_ = %s.mirror(func(%s *%s) error {\n",
				opts.ReceiverName,
				mirrorReceiverName,
				targetName(opts, opts.TargetTypeName),
			)
			fmt.Fprintf(out, "\t\treturn %s.%s(%s)\n", mirrorReceiverName, query.methodName, args)
			out.WriteString("\t})\n")
			out.WriteString("\treturn nil\n")
			return
		}
		fmt.Fprintf(
			out,
			"\treturn %s.mirror(func(%s *%s) error {\n",
			opts.ReceiverName,
			mirrorReceiverName,
			targetName(opts, opts.TargetTypeName),
		)
		fmt.Fprintf(out, "\t\treturn %s.%s(%s)\n", mirrorReceiverName, query.methodName, args)
		out.WriteString("\t})\n")
		return
	}

	resultVars := resultVariables(len(nonErrorResults))
	fmt.Fprintf(out, "\t%s := %s.main.%s(%s)\n",
		strings.Join(appendResultError(resultVars), ", "),
		opts.ReceiverName,
		query.methodName,
		args,
	)
	out.WriteString("\tif err != nil {\n")
	for idx, result := range nonErrorResults {
		fmt.Fprintf(out, "\t\tvar zero%d %s\n", idx, result)
	}
	out.WriteString("\t\treturn ")
	for idx := range nonErrorResults {
		if idx > 0 {
			out.WriteString(", ")
		}
		fmt.Fprintf(out, "zero%d", idx)
	}
	out.WriteString(", err\n")
	out.WriteString("\t}\n")

	if !mirror {
		fmt.Fprintf(out, "\treturn %s, nil\n", strings.Join(resultVars, ", "))
		return
	}
	if opts.IgnoreMirrorError {
		fmt.Fprintf(
			out,
			"\t_ = %s.mirror(func(%s *%s) error {\n",
			opts.ReceiverName,
			mirrorReceiverName,
			targetName(opts, opts.TargetTypeName),
		)
		fmt.Fprintf(out, "\t\t%s := %s.%s(%s)\n",
			strings.Join(discardVariables(len(nonErrorResults)+1), ", "),
			mirrorReceiverName,
			query.methodName,
			args,
		)
		out.WriteString("\t\treturn err\n")
		out.WriteString("\t})\n")
		fmt.Fprintf(out, "\treturn %s, nil\n", strings.Join(resultVars, ", "))
		return
	}

	fmt.Fprintf(
		out,
		"\t%s := %s.mirror(func(%s *%s) error {\n",
		mirrorErrorName,
		opts.ReceiverName,
		mirrorReceiverName,
		targetName(opts, opts.TargetTypeName),
	)
	fmt.Fprintf(out, "\t\t%s := %s.%s(%s)\n",
		strings.Join(discardVariables(len(nonErrorResults)+1), ", "),
		mirrorReceiverName,
		query.methodName,
		args,
	)
	out.WriteString("\t\treturn err\n")
	out.WriteString("\t})\n")
	fmt.Fprintf(out, "\treturn %s, %s\n", strings.Join(resultVars, ", "), mirrorErrorName)
}

func callArguments(params []argument) string {
	args := make([]string, 0, len(params))
	for _, param := range params {
		args = append(args, param.name)
	}
	return strings.Join(args, ", ")
}

func lastResultIsError(results []string) bool {
	return len(results) > 0 && results[len(results)-1] == resultErrorName
}

func resultVariables(count int) []string {
	vars := make([]string, count)
	for idx := range vars {
		vars[idx] = fmt.Sprintf("rv%d", idx)
	}
	return vars
}

func appendResultError(vars []string) []string {
	out := make([]string, 0, len(vars)+1)
	out = append(out, vars...)
	out = append(out, "err")
	return out
}

func discardVariables(count int) []string {
	vars := make([]string, count)
	for idx := range count - 1 {
		vars[idx] = "_"
	}
	vars[count-1] = "err"
	return vars
}

