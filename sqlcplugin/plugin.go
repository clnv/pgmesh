type generatedQuery struct {
	methodName string
	kind       queryKind
	params     []argument
	results    []string
	route      *shardRoute
}

type shardRoute struct {
	name       string
	methodName string
	operands   []routeOperand
}

type routeAnnotation struct {
	name     string
	operands []string
}

type routeOperand struct {
	name       string
	typ        string
	expression string
}

func collectShardRoutes(queries []generatedQuery) ([]shardRoute, error) {
	byName := make(map[string]shardRoute)
	for _, query := range queries {
		if query.route == nil {
			continue
		}
		if previous, ok := byName[query.route.methodName]; ok {
			if previous.name != query.route.name {
				return nil, fmt.Errorf(
					"shard routes %s and %s generate the same resolver method %s",
					previous.name, query.route.name, query.route.methodName,
				)
			}
			if !sameRouteSignature(previous, *query.route) {
				return nil, fmt.Errorf(
					"shard route %s is reused with incompatible parameter types",
					query.route.name,
				)
			}
			continue
		}
		byName[query.route.methodName] = *query.route
	}
	routes := make([]shardRoute, 0, len(byName))
	for _, route := range byName {
		routes = append(routes, route)
	}
	sort.Slice(routes, func(i, j int) bool { return routes[i].methodName < routes[j].methodName })
	return routes, nil
}

func sameRouteSignature(left, right shardRoute) bool {
	if len(left.operands) != len(right.operands) {
		return false
	}
	for index := range left.operands {
		if left.operands[index].typ != right.operands[index].typ {
			return false
		}
	}
	return true
}

type queryKind string

const (
	queryKindRead  queryKind = "read"
	queryKindWrite queryKind = "write"
)

type argument struct {
	name string
	typ  string
}

func paramsSignature(params []argument) string {
	parts := make([]string, 0, len(params))
	for _, param := range params {
		parts = append(parts, param.name+" "+param.typ)
	}
	return strings.Join(parts, ", ")
}

func resultsSignature(results []string) string {
	switch len(results) {
	case 0:
		return ""
	case 1:
		return " " + results[0]
	default:
		return " (" + strings.Join(results, ", ") + ")"
	}
}

type queryValue struct {
	emit        bool           `exhaustruct:"optional"`
	emitPointer bool           `exhaustruct:"optional"`
	name        string         `exhaustruct:"optional"`
	dbName      string         `exhaustruct:"optional"`
	structType  *goStruct      `exhaustruct:"optional"`
	typ         string         `exhaustruct:"optional"`
	column      *plugin.Column `exhaustruct:"optional"`
}

func (v queryValue) isEmpty() bool {
	return v.typ == "" && v.name == "" && v.structType == nil
}

func (v queryValue) defineType(opts *Options) string {
	typ := v.typ
	if typ == "" && v.structType != nil {
		typ = targetName(opts, v.structType.name)
	}
	if v.emitPointer && v.structType != nil {
		return "*" + typ
	}
	return typ
}

func (v queryValue) pairs(opts *Options) []argument {
	if v.isEmpty() {
		return nil
	}
	if !v.emit && v.structType != nil {
		out := make([]argument, 0, len(v.structType.fields))
		for _, field := range v.structType.fields {
			out = append(out, argument{
				name: escape(lowerTitle(field.name)),
				typ:  field.typ,
			})
		}
		return out
	}
	return []argument{{
		name: escape(v.name),
		typ:  v.defineType(opts),
	}}
}

func (v queryValue) slicePair(opts *Options) argument {
	return argument{name: escape(v.name), typ: "[]" + v.defineType(opts)}
}

type goStruct struct {
	table  *plugin.Identifier
	name   string
	fields []goField
}

type goField struct {
	name   string
	dbName string
	typ    string
	column *plugin.Column
}

func buildStructs(req *plugin.GenerateRequest, opts *Options, resolver *typeResolver) []goStruct {
	var structs []goStruct
	for _, schema := range req.GetCatalog().GetSchemas() {
		if skippedSchema(schema.GetName()) {
			continue
		}
		for _, table := range schema.GetTables() {
			tableName := table.GetRel().GetName()
			if schema.GetName() != req.GetCatalog().GetDefaultSchema() {
				tableName = schema.GetName() + "_" + tableName
			}
			if !opts.EmitExactTableNames {
				tableName = singular(tableName)
			}

			strct := goStruct{
				table: &plugin.Identifier{
					Catalog: table.GetRel().GetCatalog(),
					Schema:  schema.GetName(),
					Name:    table.GetRel().GetName(),
				},
				name:   structName(tableName, opts),
				fields: nil,
			}
			for _, column := range table.GetColumns() {
				strct.fields = append(strct.fields, goField{
					name:   structName(column.GetName(), opts),
					dbName: column.GetName(),
					typ:    resolver.goType(column),
					column: column,
				})
			}
			structs = append(structs, strct)
		}
	}
	sort.Slice(structs, func(i, j int) bool {
		return structs[i].name < structs[j].name
	})
	return structs
}

func skippedSchema(name string) bool {
	return name == "pg_catalog" || name == "information_schema"
}

func singular(name string) string {
	switch strings.ToLower(name) {
	case "campus", "meta", "metadata":
		return name
	case "calories":
		return "calorie"
	case "waves":
		return "wave"
	default:
		return inflection.Singular(name)
	}
}

func buildQueries(
	req *plugin.GenerateRequest,
	opts *Options,
	resolver *typeResolver,
	structs []goStruct,
) ([]generatedQuery, error) {
	out := make([]generatedQuery, 0, len(req.GetQueries()))
	for _, query := range req.GetQueries() {
		if query.GetName() == "" || query.GetCmd() == "" {
			continue
		}

		arg, err := buildQueryArg(query, opts, resolver)
		if err != nil {
			return nil, err
		}
		ret, err := buildQueryReturn(req, query, opts, resolver, structs)
		if err != nil {
			return nil, err
		}

		params := []argument{{name: "ctx", typ: "context.Context"}}
		if query.GetCmd() == cmdCopyFrom || strings.HasPrefix(query.GetCmd(), ":batch") {
			if !arg.isEmpty() {
				params = append(params, arg.slicePair(opts))
			}
		} else {
			params = append(params, arg.pairs(opts)...)
		}

		results, err := resultTypes(query, ret, opts)
		if err != nil {
			return nil, err
		}
		for _, result := range results {
			resolver.addImportsForType(result)
		}
		for _, param := range params {
			resolver.addImportsForType(param.typ)
		}

		kind, annotation, err := classifyQuery(query)
		if err != nil {
			return nil, err
		}
		var route *shardRoute
		if annotation != nil {
			if query.GetCmd() == cmdCopyFrom || strings.HasPrefix(query.GetCmd(), ":batch") {
				return nil, fmt.Errorf(
					"query %s uses %s and cannot declare shard metadata; partition the batch and use node-level wrappers",
					query.GetName(), query.GetCmd(),
				)
			}
			route, err = resolveShardRoute(query, arg, annotation, opts)
			if err != nil {
				return nil, err
			}
			if !lastResultIsError(results) {
				return nil, fmt.Errorf("query %s cannot be shard-routed because its generated result has no error", query.GetName())
			}
		}

		out = append(out, generatedQuery{
			methodName: query.GetName(),
			kind:       kind,
			params:     params,
			results:    results,
			route:      route,
		})
	}
	sort.Slice(out, func(i, j int) bool {
		return out[i].methodName < out[j].methodName
	})
	return out, nil
}

func classifyQuery(query *plugin.Query) (queryKind, *routeAnnotation, error) {
	if err := validateRawAnnotationOrder(query); err != nil {
		return "", nil, err
	}
	comments := queryComments(query)
	if len(comments) == 0 {
		return "", nil, fmt.Errorf(
			"query %s is missing required kind annotation; add -- kind: read or -- kind: write immediately after -- name",
			query.GetName(),
		)
	}
	kind, err := queryKindFromComment(query.GetName(), comments[0])
	if err != nil {
		return "", nil, err
	}

	var route *routeAnnotation
	if len(comments) > 1 && strings.HasPrefix(strings.ToLower(normalizeComment(comments[1])), "shard:") {
		route, err = parseShardAnnotation(query.GetName(), comments[1])
		if err != nil {
			return "", nil, err
		}
	}
	start := 1
	if route != nil {
		start = 2
	}
	for _, comment := range comments[start:] {
		if strings.HasPrefix(strings.ToLower(normalizeComment(comment)), "shard:") {
			return "", nil, fmt.Errorf(
				"query %s shard annotation must immediately follow its kind annotation",
				query.GetName(),
			)
		}
	}
	return kind, route, nil
}

func validateRawAnnotationOrder(query *plugin.Query) error {
	lines := strings.Split(query.GetText(), "\n")
	nameLine := -1
	for index, rawLine := range lines {
		if strings.HasPrefix(strings.ToLower(strings.TrimSpace(rawLine)), "-- name:") {
			nameLine = index
			break
		}
	}
	if nameLine == -1 {
		return nil
	}
	if nameLine+1 >= len(lines) || !strings.HasPrefix(
		strings.ToLower(normalizeComment(lines[nameLine+1])),
		"kind:",
	) || !strings.HasPrefix(strings.TrimSpace(lines[nameLine+1]), "--") {
		return fmt.Errorf(
			"query %s kind annotation must immediately follow its sqlc name annotation",
			query.GetName(),
		)
	}
	for index := nameLine + 2; index < len(lines); index++ {
		if !strings.HasPrefix(strings.ToLower(normalizeComment(lines[index])), "shard:") {
			continue
		}
		if index != nameLine+2 || !strings.HasPrefix(strings.TrimSpace(lines[index]), "--") {
			return fmt.Errorf(
				"query %s shard annotation must immediately follow its kind annotation",
				query.GetName(),
			)
		}
		break
	}
	return nil
}

func queryComments(query *plugin.Query) []string {
	if comments := query.GetComments(); len(comments) > 0 {
		return comments
	}
	var comments []string
	for rawLine := range strings.SplitSeq(query.GetText(), "\n") {
		line := strings.TrimSpace(rawLine)
		if line == "" || strings.HasPrefix(strings.ToLower(line), "-- name:") {
			continue
		}
		if strings.HasPrefix(line, "--") {
			comments = append(comments, line)
			continue
		}
		break
	}
	return comments
}

func parseShardAnnotation(queryName, comment string) (*routeAnnotation, error) {
	normalized := normalizeComment(comment)
	value := strings.TrimSpace(normalized[len("shard:"):])
	open := strings.IndexByte(value, '(')
	if open <= 0 || !strings.HasSuffix(value, ")") {
		return nil, fmt.Errorf(
			"query %s has malformed shard annotation %q; expected route(parameter, ...)",
			queryName, value,
		)
	}
	name := strings.TrimSpace(value[:open])
	if !validRouteIdentifier(name) {
		return nil, fmt.Errorf("query %s has invalid shard route name %q", queryName, name)
	}
	body := strings.TrimSpace(value[open+1 : len(value)-1])
	if body == "" {
		return &routeAnnotation{name: name, operands: nil}, nil
	}
	parts := strings.Split(body, ",")
	operands := make([]string, 0, len(parts))
	seen := make(map[string]struct{}, len(parts))
	for _, part := range parts {
		operand := strings.TrimSpace(part)
		if !validRouteIdentifier(operand) {
			return nil, fmt.Errorf("query %s has invalid shard operand %q", queryName, operand)
		}
		if _, ok := seen[operand]; ok {
			return nil, fmt.Errorf("query %s repeats shard operand %q", queryName, operand)
		}
		seen[operand] = struct{}{}
		operands = append(operands, operand)
	}
	return &routeAnnotation{name: name, operands: operands}, nil
}

func validRouteIdentifier(value string) bool {
	if value == "" {
		return false
	}
	for index, r := range value {
		if unicode.IsLetter(r) || r == '_' || index > 0 && unicode.IsDigit(r) {
			continue
		}
		return false
	}
	return true
}

func queryKindFromComment(queryName, comment string) (queryKind, error) {
	comment = normalizeComment(comment)
	lower := strings.ToLower(comment)
	if !strings.HasPrefix(lower, "kind:") {
		return "", fmt.Errorf(
			"query %s first comment must be kind annotation; add -- kind: read or -- kind: write before other comments",
			queryName,
		)
	}

	value := strings.TrimSpace(comment[len("kind:"):])
	switch strings.ToLower(value) {
	case string(queryKindRead):
		return queryKindRead, nil
	case string(queryKindWrite):
		return queryKindWrite, nil
	default:
		return "", fmt.Errorf("query %s has invalid kind annotation %q; expected read or write", queryName, value)
	}
}

func normalizeComment(comment string) string {
	comment = strings.TrimSpace(comment)
	comment = strings.TrimPrefix(comment, "--")
	comment = strings.TrimPrefix(comment, "//")
	comment = strings.TrimSpace(comment)
	if strings.HasPrefix(comment, "/*") && strings.HasSuffix(comment, "*/") {
		comment = strings.TrimPrefix(comment, "/*")
		comment = strings.TrimSuffix(comment, "*/")
	}
	return strings.TrimSpace(comment)
}

func buildQueryArg(query *plugin.Query, opts *Options, resolver *typeResolver) (queryValue, error) {
	params := query.GetParams()
	limit := int(*opts.QueryParameterLimit)
	if len(params) == 1 && limit != 0 {
		param := params[0]
		return queryValue{
			name:   escape(paramName(param)),
			dbName: param.GetColumn().GetName(),
			typ:    resolver.goType(param.GetColumn()),
			column: param.GetColumn(),
		}, nil
	}
	if len(params) == 0 {
		return queryValue{}, nil
	}

	columns := make([]goColumn, 0, len(params))
	for _, param := range params {
		columns = append(columns, goColumn{
			id:     int(param.GetNumber()),
			column: param.GetColumn(),
		})
	}
	strct, err := columnsToStruct(query.GetName()+"Params", columns, opts, resolver, false)
	if err != nil {
		return queryValue{}, err
	}
	emit := len(params) > limit || query.GetCmd() == cmdCopyFrom

	return queryValue{
		emit:        emit,
		emitPointer: opts.EmitParamsStructPointers,
		name:        "arg",
		structType:  strct,
	}, nil
}

func resolveShardRoute(
	query *plugin.Query,
	arg queryValue,
	annotation *routeAnnotation,
	opts *Options,
) (*shardRoute, error) {
	route := &shardRoute{
		name:       annotation.name,
		methodName: routeMethodName(annotation.name, opts),
		operands:   make([]routeOperand, 0, len(annotation.operands)),
	}
	if route.methodName == "" {
		return nil, fmt.Errorf("query %s shard route %q does not produce a Go method name", query.GetName(), annotation.name)
	}
	first, _ := utf8.DecodeRuneInString(route.methodName)
	if !validRouteIdentifier(route.methodName) || !unicode.IsUpper(first) {
		return nil, fmt.Errorf(
			"query %s shard route %q generates non-exported or invalid resolver method %q",
			query.GetName(), annotation.name, route.methodName,
		)
	}
	seenNames := make(map[string]struct{}, len(annotation.operands))
	for _, dbName := range annotation.operands {
		operand, ok := routeOperandForArg(arg, dbName, opts)
		if !ok {
			return nil, fmt.Errorf(
				"query %s shard operand %q does not match a SQL parameter",
				query.GetName(), dbName,
			)
		}
		if _, ok := seenNames[operand.name]; ok {
			return nil, fmt.Errorf(
				"query %s shard operands generate duplicate Go parameter %q",
				query.GetName(), operand.name,
			)
		}
		seenNames[operand.name] = struct{}{}
		route.operands = append(route.operands, operand)
	}
	return route, nil
}

func routeMethodName(name string, opts *Options) string {
	if replacement := opts.Rename[name]; replacement != "" {
		return replacement
	}
	parts := splitIdentifier(name)
	for index, part := range parts {
		if strings.EqualFold(part, "p2p") {
			parts[index] = "P2P"
			continue
		}
		parts[index] = structName(part, opts)
	}
	return strings.Join(parts, "")
}

func routeOperandForArg(arg queryValue, dbName string, opts *Options) (routeOperand, bool) {
	if arg.structType != nil {
		for _, field := range arg.structType.fields {
			if field.dbName != dbName {
				continue
			}
			expression := escape(lowerTitle(field.name))
			if arg.emit {
				expression = escape(arg.name) + "." + field.name
			}
			return routeOperand{
				name:       escape(lowerTitle(structName(dbName, opts))),
				typ:        field.typ,
				expression: expression,
			}, true
		}
		return routeOperand{name: "", typ: "", expression: ""}, false
	}
	if arg.dbName != dbName {
		return routeOperand{name: "", typ: "", expression: ""}, false
	}
	return routeOperand{
		name:       escape(lowerTitle(structName(dbName, opts))),
		typ:        arg.typ,
		expression: escape(arg.name),
	}, true
}

func buildQueryReturn(
	req *plugin.GenerateRequest,
	query *plugin.Query,
	opts *Options,
	resolver *typeResolver,
	structs []goStruct,
) (queryValue, error) {
	if !queryReturnsData(query.GetCmd()) {
		return queryValue{}, nil
	}
	columns := query.GetColumns()
	if len(columns) == 1 && columns[0].GetEmbedTable() == nil {
		column := columns[0]
		name := strings.ReplaceAll(columnName(column, 0), "$", "_")
		return queryValue{
			name:   escape(name),
			dbName: name,
			typ:    resolver.goType(column),
			column: column,
		}, nil
	}
	if len(columns) == 0 {
		return queryValue{}, nil
	}

	var model *goStruct
	for idx := range structs {
		strct := &structs[idx]
		if queryColumnsMatchStruct(req, opts, resolver, columns, strct) {
			model = strct
			break
		}
	}

	emit := false
	if model == nil {
		goColumns := make([]goColumn, 0, len(columns))
		for idx, column := range columns {
			goColumns = append(goColumns, goColumn{
				id:     idx,
				column: column,
			})
		}
		var err error
		model, err = columnsToStruct(query.GetName()+"Row", goColumns, opts, resolver, true)
		if err != nil {
			return queryValue{}, err
		}
		emit = true
	}
	return queryValue{
		emit:        emit,
		emitPointer: opts.EmitResultStructPointers,
		name:        "i",
		structType:  model,
	}, nil
}

func queryReturnsData(cmd string) bool {
	switch cmd {
	case ":one", ":many", ":batchone", ":batchmany":
		return true
	default:
		return false
	}
}

func resultTypes(query *plugin.Query, ret queryValue, opts *Options) ([]string, error) {
	switch query.GetCmd() {
	case ":one":
		return []string{ret.defineType(opts), resultErrorName}, nil
	case ":many":
		return []string{"[]" + ret.defineType(opts), resultErrorName}, nil
	case ":exec":
		return []string{resultErrorName}, nil
	case ":execrows", cmdCopyFrom:
		return []string{"int64", resultErrorName}, nil
	case ":execresult":
		return []string{"pgconn.CommandTag", resultErrorName}, nil
	case ":batchexec", ":batchone", ":batchmany":
		return []string{"*" + targetName(opts, query.GetName()+"BatchResults")}, nil
	default:
		return nil, fmt.Errorf("unsupported query command %q for %s", query.GetCmd(), query.GetName())
	}
}

func queryColumnsMatchStruct(
	req *plugin.GenerateRequest,
	opts *Options,
	resolver *typeResolver,
	columns []*plugin.Column,
	strct *goStruct,
) bool {
	if len(strct.fields) != len(columns) {
		return false
	}
	for idx, field := range strct.fields {
		column := columns[idx]
		if field.name != structName(columnName(column, idx), opts) {
			return false
		}
		if field.typ != resolver.goType(column) {
			return false
		}
		if !sameTableName(column.GetTable(), strct.table, req.GetCatalog().GetDefaultSchema()) {
			return false
		}
	}
	return true
}

type goColumn struct {
	id     int
	column *plugin.Column
}

func columnsToStruct(
	name string,
	columns []goColumn,
	opts *Options,
	resolver *typeResolver,
	useID bool,
) (*goStruct, error) {
	strct := &goStruct{
		table:  nil,
		name:   name,
		fields: nil,
	}
	seen := map[string][]int{}
	suffixes := map[int]int{}
	for idx, column := range columns {
		columnName := columnName(column.column, idx)
		fieldName := structName(columnName, opts)
		baseFieldName := fieldName

		suffix := 0
		if existing, ok := suffixes[column.id]; ok && useID {
			suffix = existing
		} else if count := len(seen[fieldName]); count > 0 && !column.column.GetIsNamedParam() {
			suffix = count + 1
		}
		suffixes[column.id] = suffix
		if suffix > 0 {
			fieldName = fmt.Sprintf("%s_%d", fieldName, suffix)
		}

		strct.fields = append(strct.fields, goField{
			name:   fieldName,
			dbName: columnName,
			typ:    resolver.goType(column.column),
			column: column.column,
		})
		seen[baseFieldName] = append(seen[baseFieldName], idx)
	}
	return strct, checkIncompatibleFieldTypes(strct.fields)
}

func checkIncompatibleFieldTypes(fields []goField) error {
	fieldTypes := map[string]string{}
	for _, field := range fields {
		if existing, ok := fieldTypes[field.name]; ok && existing != field.typ {
			return fmt.Errorf("named param %s has incompatible types: %s, %s", field.name, existing, field.typ)
		}
		fieldTypes[field.name] = field.typ
	}
	return nil
}

func columnName(column *plugin.Column, pos int) string {
	if column.GetName() != "" {
		return column.GetName()
	}
	return fmt.Sprintf("column_%d", pos+1)
}

func paramName(param *plugin.Parameter) string {
	if param.GetColumn().GetName() != "" {
		return argName(param.GetColumn().GetName())
	}
	return fmt.Sprintf("dollar_%d", param.GetNumber())
}

func argName(name string) string {
	var out strings.Builder
	for idx, part := range strings.Split(name, "_") {
		switch {
		case idx == 0:
			out.WriteString(strings.ToLower(part))
		case strings.EqualFold(part, "id"):
			out.WriteString("ID")
		default:
			out.WriteString(title(part))
		}
	}
	return out.String()
}

func structName(name string, opts *Options) string {
	if replacement := opts.Rename[name]; replacement != "" {
		return replacement
	}
	parts := splitIdentifier(name)
	for idx, part := range parts {
		lower := strings.ToLower(part)
		if strings.EqualFold(lower, "id") {
			parts[idx] = "ID"
			continue
		}
		parts[idx] = title(lower)
	}
	result := strings.Join(parts, "")
	first, _ := utf8.DecodeRuneInString(result)
	if unicode.IsDigit(first) {
		return "_" + result
	}
	return result
}

func splitIdentifier(name string) []string {
	var parts []string
	var current strings.Builder
	for _, r := range name {
		if unicode.IsLetter(r) || unicode.IsDigit(r) {
			current.WriteRune(r)
			continue
		}
		if current.Len() > 0 {
			parts = append(parts, current.String())
			current.Reset()
		}
	}
	if current.Len() > 0 {
		parts = append(parts, current.String())
	}
	return parts
}

func title(s string) string {
	if s == "" {
		return s
	}
	runes := []rune(s)
	runes[0] = unicode.ToUpper(runes[0])
	return string(runes)
}

func lowerTitle(s string) string {
	if s == "" {
		return s
	}
	runes := []rune(s)
	runes[0] = unicode.ToLower(runes[0])
	return string(runes)
}

func escape(name string) string {
	if goKeywords[name] {
		return name + "_"
	}
	return name
}

var goKeywords = map[string]bool{
	"break":       true,
	"default":     true,
	"func":        true,
	"interface":   true,
	"select":      true,
	"case":        true,
	"defer":       true,
	"go":          true,
	"map":         true,
	"struct":      true,
	"chan":        true,
	"else":        true,
	"goto":        true,
	"package":     true,
	"switch":      true,
	"const":       true,
	"fallthrough": true,
	"if":          true,
	"range":       true,
	"type":        true,
	"continue":    true,
	"for":         true,
	"import":      true,
	"return":      true,
	"var":         true,
}

func sameTableName(left, right *plugin.Identifier, defaultSchema string) bool {
	if left == nil || right == nil {
		return false
	}
	leftSchema := left.GetSchema()
	if leftSchema == "" {
		leftSchema = defaultSchema
	}
	rightSchema := right.GetSchema()
	if rightSchema == "" {
		rightSchema = defaultSchema
	}
	return left.GetCatalog() == right.GetCatalog() &&
		leftSchema == rightSchema &&
		left.GetName() == right.GetName()
}

func (r *typeResolver) goType(column *plugin.Column) string {
	for idx := range r.opts.Overrides {
		override := &r.opts.Overrides[idx]
		if override.Column == "" || !override.matchesColumn(column, r.req.GetCatalog().GetDefaultSchema()) {
			continue
		}
		typ := r.overrideType(override)
		if column.GetIsSqlcSlice() {
			return "[]" + typ
		}
		return typ
	}
	for idx := range r.opts.Overrides {
		override := &r.opts.Overrides[idx]
		if !override.matchesColumn(column, r.req.GetCatalog().GetDefaultSchema()) {
			continue
		}
		typ := r.overrideType(override)
		if column.GetIsSqlcSlice() {
			typ = "[]" + typ
		} else if column.GetIsArray() {
			typ = strings.Repeat("[]", int(column.GetArrayDims())) + typ
		}
		return typ
	}

	typ := r.goInnerType(column)
	if column.GetIsSqlcSlice() {
		typ = "[]" + typ
	} else if column.GetIsArray() {
		typ = strings.Repeat("[]", int(column.GetArrayDims())) + typ
	}
	return typ
}

func (r *typeResolver) overrideType(override *override) string {
	typ := override.typeName
	if r.opts.InternalImportPath == "" || override.importPath != "" || typeQualifier(typ) != "" {
		return typ
	}
	prefixLength := 0
	for strings.HasPrefix(typ[prefixLength:], "[]") || strings.HasPrefix(typ[prefixLength:], "*") {
		if strings.HasPrefix(typ[prefixLength:], "[]") {
			prefixLength += 2
			continue
		}
		prefixLength++
	}
	base := typ[prefixLength:]
	if base == "interface{}" || types.Universe.Lookup(base) != nil {
		return typ
	}
	return typ[:prefixLength] + targetName(r.opts, base)
}

func (r *typeResolver) addImportsForType(typ string) {
	r.imports.addForType(typ)
	for idx := range r.opts.Overrides {
		override := &r.opts.Overrides[idx]
		if override.importPath == "" {
			continue
		}
		qualifier := typeQualifier(override.typeName)
		if qualifier == "" || !typeUsesQualifier(typ, qualifier) {
			continue
		}
		r.imports.addNamed(override.importAlias, override.importPath)
	}
}

func typeQualifier(typ string) string {
	typ = strings.TrimPrefix(typ, "[]")
	typ = strings.TrimPrefix(typ, "*")
	before, _, ok := strings.Cut(typ, ".")
	if !ok {
		return ""
	}
	return before
}

func (r *typeResolver) goInnerType(column *plugin.Column) string {
	switch r.req.GetSettings().GetEngine() {
	case "postgresql":
		return r.postgresType(column)
	default:
		return emptyInterfaceType
	}
}

func (r *typeResolver) postgresType(column *plugin.Column) string {
	columnType := dataType(column.GetType())
	notNull := column.GetNotNull() || column.GetIsArray()
	driver := sqlDriver(r.opts.SQLPackage)
	emitPointersForNull := driver.isPGX() && r.opts.EmitPointersForNullTypes
	emitPointersForNullEnums := emitPointersForNull
	if r.opts.EmitPointersForNullEnumType != nil {
		emitPointersForNullEnums = driver.isPGX() && *r.opts.EmitPointersForNullEnumType
	}

	switch columnType {
	case "serial", "serial4", "pg_catalog.serial4", "integer", "int", "int4", "pg_catalog.int4":
		return nullableType("int32", "*int32", "pgtype.Int4", "sql.NullInt32", notNull, emitPointersForNull, driver)
	case "bigserial", "serial8", "pg_catalog.serial8", "bigint", "int8", "pg_catalog.int8":
		return nullableType("int64", "*int64", "pgtype.Int8", "sql.NullInt64", notNull, emitPointersForNull, driver)
	case "smallserial", "serial2", "pg_catalog.serial2", "smallint", "int2", "pg_catalog.int2":
		return nullableType("int16", "*int16", "pgtype.Int2", "sql.NullInt16", notNull, emitPointersForNull, driver)
	case "float", "double precision", "float8", "pg_catalog.float8":
		return nullableType("float64", "*float64", "pgtype.Float8", "sql.NullFloat64", notNull, emitPointersForNull, driver)
	case "real", "float4", "pg_catalog.float4":
		return nullableType("float32", "*float32", "pgtype.Float4", "sql.NullFloat64", notNull, emitPointersForNull, driver)
	case "boolean", "bool", "pg_catalog.bool":
		return nullableType("bool", "*bool", "pgtype.Bool", "sql.NullBool", notNull, emitPointersForNull, driver)
	case "text", "pg_catalog.varchar", "pg_catalog.bpchar", "varchar", "string", "citext", "name":
		return nullableType("string", "*string", "pgtype.Text", "sql.NullString", notNull, emitPointersForNull, driver)
	case "bytea", "blob", "pg_catalog.bytea":
		return "[]byte"
	case "json", "pg_catalog.json", "jsonb", "pg_catalog.jsonb":
		if driver == sqlDriverPGXV5 {
			return "[]byte"
		}
		if notNull {
			return "json.RawMessage"
		}
		return "pqtype.NullRawMessage"
	case "date":
		if driver == sqlDriverPGXV5 {
			return "pgtype.Date"
		}
		return nullableStdlibType("time.Time", "*time.Time", "sql.NullTime", notNull, emitPointersForNull)
	case "pg_catalog.time":
		if driver == sqlDriverPGXV5 {
			return "pgtype.Time"
		}
		return nullableStdlibType("time.Time", "*time.Time", "sql.NullTime", notNull, emitPointersForNull)
	case "pg_catalog.timetz":
		return nullableStdlibType("time.Time", "*time.Time", "sql.NullTime", notNull, emitPointersForNull)
	case "pg_catalog.timestamp", "timestamp":
		if driver == sqlDriverPGXV5 {
			return "pgtype.Timestamp"
		}
		return nullableStdlibType("time.Time", "*time.Time", "sql.NullTime", notNull, emitPointersForNull)
	case "pg_catalog.timestamptz", "timestamptz":
		if driver == sqlDriverPGXV5 {
			return "pgtype.Timestamptz"
		}
		return nullableStdlibType("time.Time", "*time.Time", "sql.NullTime", notNull, emitPointersForNull)
	case "uuid":
		if driver == sqlDriverPGXV5 {
			return "pgtype.UUID"
		}
		return nullableStdlibType("uuid.UUID", "*uuid.UUID", "uuid.NullUUID", notNull, emitPointersForNull)
	case "numeric", "pg_catalog.numeric", "money":
		if driver.isPGX() {
			return "pgtype.Numeric"
		}
		return nullableStdlibType("string", "*string", "sql.NullString", notNull, emitPointersForNull)
	case "inet":
		if notNull {
			return "netip.Addr"
		}
		return "*netip.Addr"
	case "cidr":
		if notNull {
			return "netip.Prefix"
		}
		return "*netip.Prefix"
	case "macaddr", "macaddr8":
		return "net.HardwareAddr"
	case "ltree", "lquery", "ltxtquery":
		return nullableType("string", "*string", "pgtype.Text", "sql.NullString", notNull, emitPointersForNull, driver)
	case "interval", "pg_catalog.interval":
		return "pgtype.Interval"
	case "daterange":
		return "pgtype.Range[pgtype.Date]"
	case "datemultirange":
		return "pgtype.Multirange[pgtype.Range[pgtype.Date]]"
	case "tsrange":
		return "pgtype.Range[pgtype.Timestamp]"
	case "tsmultirange":
		return "pgtype.Multirange[pgtype.Range[pgtype.Timestamp]]"
	case "tstzrange":
		return "pgtype.Range[pgtype.Timestamptz]"
	case "tstzmultirange":
		return "pgtype.Multirange[pgtype.Range[pgtype.Timestamptz]]"
	case "numrange":
		return "pgtype.Range[pgtype.Numeric]"
	case "nummultirange":
		return "pgtype.Multirange[pgtype.Range[pgtype.Numeric]]"
	case "int4range":
		return "pgtype.Range[pgtype.Int4]"
	case "int4multirange":
		return "pgtype.Multirange[pgtype.Range[pgtype.Int4]]"
	case "int8range":
		return "pgtype.Range[pgtype.Int8]"
	case "int8multirange":
		return "pgtype.Multirange[pgtype.Range[pgtype.Int8]]"
	case "hstore":
		return "pgtype.Hstore"
	case "bit", "varbit", "pg_catalog.bit", "pg_catalog.varbit":
		return "pgtype.Bits"
	case "cid", "oid":
		return "pgtype.Uint32"
	case "tid":
		return "pgtype.TID"
	case "xid":
		return "pgtype.Uint32"
	case "xid8":
		return "pgtype.Uint64"
	case "box":
		return "pgtype.Box"
	case "circle":
		return "pgtype.Circle"
	case "line":
		return "pgtype.Line"
	case "lseg":
		return "pgtype.Lseg"
	case "path":
		return "pgtype.Path"
	case "point":
		return "pgtype.Point"
	case "polygon":
		return "pgtype.Polygon"
	case "vector":
		if emitPointersForNull {
			return "*pgvector.Vector"
		}
		return "pgvector.Vector"
	case "void", "any":
		return emptyInterfaceType
	default:
		return r.customPostgresType(columnType, notNull, emitPointersForNull, emitPointersForNullEnums)
	}
}

func (r *typeResolver) customPostgresType(
	columnType string,
	notNull bool,
	emitPointersForNull bool,
	emitPointersForNullEnums bool,
) string {
	parts := strings.Split(columnType, ".")
	var schemaName, typeName string
	switch len(parts) {
	case 1:
		schemaName = r.req.GetCatalog().GetDefaultSchema()
		typeName = parts[0]
	case 2:
		schemaName = parts[0]
		typeName = parts[1]
	case 3:
		schemaName = parts[1]
		typeName = parts[2]
	default:
		return emptyInterfaceType
	}

	for _, schema := range r.req.GetCatalog().GetSchemas() {
		if schema.GetName() != schemaName || skippedSchema(schema.GetName()) {
			continue
		}
		for _, enum := range schema.GetEnums() {
			if enum.GetName() != typeName {
				continue
			}
			name := enum.GetName()
			if schemaName != r.req.GetCatalog().GetDefaultSchema() {
				name = schemaName + "_" + name
			}
			name = structName(name, r.opts)
			if notNull {
				return targetName(r.opts, name)
			}
			if emitPointersForNullEnums {
				return "*" + targetName(r.opts, name)
			}
			return targetName(r.opts, "Null"+name)
		}
		for _, composite := range schema.GetCompositeTypes() {
			if composite.GetName() != typeName {
				continue
			}
			return nullableStdlibType("string", "*string", "sql.NullString", notNull, emitPointersForNull)
		}
	}
	return emptyInterfaceType
}

func nullableType(
	notNullType string,
	pointerType string,
	pgxType string,
	sqlNullType string,
	notNull bool,
	emitPointersForNull bool,
	driver sqlDriver,
) string {
	if notNull {
		return notNullType
	}
	if emitPointersForNull {
		return pointerType
	}
	if driver == sqlDriverPGXV5 {
		return pgxType
	}
	return sqlNullType
}

func nullableStdlibType(notNullType, pointerType, nullType string, notNull, emitPointersForNull bool) string {
	if notNull {
		return notNullType
	}
	if emitPointersForNull {
		return pointerType
	}
	return nullType
}

type sqlDriver string

const (
	sqlDriverLibPQ sqlDriver = "database/sql"
	sqlDriverPGXV4 sqlDriver = "pgx/v4"
	sqlDriverPGXV5 sqlDriver = "pgx/v5"
)

func (d sqlDriver) isPGX() bool {
	return d == sqlDriverPGXV4 || d == sqlDriverPGXV5
}

type override struct {
	GoType                 goType `json:"go_type"`
	DBType                 string `json:"db_type"`
	DeprecatedPostgresType string `json:"postgres_type"`
	Nullable               bool   `json:"nullable"`
	DeprecatedNull         bool   `json:"null"`
	Unsigned               bool   `json:"unsigned"`
	Column                 string `json:"column"`

	typeName     string
	importPath   string
	importAlias  string
	columnName   string
	tableCatalog string
	tableSchema  string
	tableName    string
}

func (o *override) parse(req *plugin.GenerateRequest) error {
	if o.DeprecatedPostgresType != "" && o.DBType == "" {
		o.DBType = o.DeprecatedPostgresType
	}
	if o.DeprecatedNull {
		o.Nullable = true
	}
	if o.DBType == "" && o.Column == "" {
		return errors.New("override must specify db_type or column")
	}
	if o.DBType != "" && o.Column != "" {
		return errors.New("override cannot specify both db_type and column")
	}
	parsed, err := o.GoType.parse()
	if err != nil {
		return err
	}
	o.typeName = parsed.typeName
	o.importPath = parsed.importPath
	o.importAlias = parsed.importAlias
	if o.Column != "" {
		if err := o.parseColumn(req); err != nil {
			return err
		}
	}
	return nil
}

func (o *override) parseColumn(req *plugin.GenerateRequest) error {
	parts := strings.Split(o.Column, ".")
	defaultSchema := req.GetCatalog().GetDefaultSchema()
	if defaultSchema == "" {
		defaultSchema = "public"
	}
	switch len(parts) {
	case 2:
		o.tableSchema = defaultSchema
		o.tableName = parts[0]
		o.columnName = parts[1]
	case 3:
		o.tableSchema = parts[0]
		o.tableName = parts[1]
		o.columnName = parts[2]
	case 4:
		o.tableCatalog = parts[0]
		o.tableSchema = parts[1]
		o.tableName = parts[2]
		o.columnName = parts[3]
	default:
		return fmt.Errorf("invalid column override %q", o.Column)
	}
	for _, pattern := range []string{o.tableCatalog, o.tableSchema, o.tableName, o.columnName} {
		if pattern == "" {
			continue
		}
		if _, err := compileMatchPattern(pattern); err != nil {
			return fmt.Errorf("invalid column override %q: %w", o.Column, err)
		}
	}
	return nil
}

func (o *override) matchesColumn(column *plugin.Column, defaultSchema string) bool {
	if o.DBType != "" {
		columnType := dataType(column.GetType())
		notNull := column.GetNotNull() || column.GetIsArray()
		return dbTypesEqual(o.DBType, columnType, defaultSchema) &&
			o.Nullable != notNull &&
			o.Unsigned == column.GetUnsigned()
	}
	if o.Column == "" {
		return false
	}
	table := column.GetTable()
	if table == nil {
		return false
	}
	schema := table.GetSchema()
	if schema == "" {
		schema = defaultSchema
	}
	name := column.GetName()
	if column.GetOriginalName() != "" {
		name = column.GetOriginalName()
	}
	if o.tableCatalog != "" && !matchPattern(o.tableCatalog, table.GetCatalog()) {
		return false
	}
	return matchPattern(o.tableSchema, schema) &&
		matchPattern(o.tableName, table.GetName()) &&
		matchPattern(o.columnName, name)
}

func matchPattern(pattern, value string) bool {
	compiled, err := compileMatchPattern(pattern)
	return err == nil && compiled.MatchString(value)
}

func compileMatchPattern(pattern string) (*regexp.Regexp, error) {
	var expression strings.Builder
	escaped := false
	for _, char := range pattern {
		if escaped {
			escaped = false
			switch char {
			case '*', '?', '\\':
				expression.WriteRune('\\')
				expression.WriteRune(char)
			default:
				return nil, fmt.Errorf("invalid escaped character %q", char)
			}
			continue
		}
		switch char {
		case '\\':
			escaped = true
		case '*':
			expression.WriteString(".*")
		case '?':
			expression.WriteRune('.')
		default:
			expression.WriteString(regexp.QuoteMeta(string(char)))
		}
	}
	if escaped {
		return nil, errors.New("unterminated escape at end of pattern")
	}
	return regexp.Compile("^" + expression.String() + "$")
}

func dbTypesEqual(configured, actual, defaultSchema string) bool {
	if configured == actual {
		return true
	}
	if !strings.Contains(actual, ".") && defaultSchema != "" && configured == defaultSchema+"."+actual {
		return true
	}
	return false
}

type goType struct {
	path    string
	pkg     string
	name    string
	pointer bool
	slice   bool
	spec    string
}

func (g *goType) UnmarshalJSON(data []byte) error {
	var spec string
	if err := json.Unmarshal(data, &spec); err == nil {
		g.spec = spec
		return nil
	}
	var raw struct {
		Path    string `json:"import"`
		Package string `json:"package"`
		Name    string `json:"type"`
		Pointer bool   `json:"pointer"`
		Slice   bool   `json:"slice"`
	}
	if err := json.Unmarshal(data, &raw); err != nil {
		return err
	}
	g.path = raw.Path
	g.pkg = raw.Package
	g.name = raw.Name
	g.pointer = raw.Pointer
	g.slice = raw.Slice
	return nil
}

type parsedGoType struct {
	typeName    string
	importPath  string
	importAlias string
}

func (g goType) parse() (parsedGoType, error) {
	if g.spec != "" {
		return parseGoTypeSpec(g.spec)
	}
	if g.path == "" && g.pkg != "" {
		return parsedGoType{}, errors.New("go_type package requires an import path")
	}

	typeName := g.name
	pkg := g.pkg
	alias := ""
	if pkg == "" && g.path != "" {
		pkg = packageNameForImport(g.path)
	}
	if pkg != "" {
		typeName = pkg + "." + typeName
		if g.pkg != "" && g.pkg != packageNameForImport(g.path) {
			alias = g.pkg
		}
	}
	if g.pointer {
		typeName = "*" + typeName
	}
	if g.slice {
		typeName = "[]" + typeName
	}
	return parsedGoType{typeName: typeName, importPath: g.path, importAlias: alias}, nil
}

func parseGoTypeSpec(spec string) (parsedGoType, error) {
	lastDot := strings.LastIndex(spec, ".")
	lastSlash := strings.LastIndex(spec, "/")
	if lastDot == -1 && lastSlash == -1 {
		for _, basic := range types.Typ {
			if basic != nil && basic.Info() != 0 && basic.Info()&types.IsUntyped == 0 && basic.Name() == spec {
				return parsedGoType{typeName: spec, importPath: "", importAlias: ""}, nil
			}
		}
		return parsedGoType{}, fmt.Errorf("go_type specifier %q is not a Go basic type", spec)
	}
	if lastDot == -1 {
		return parsedGoType{}, fmt.Errorf("go_type specifier %q must use package.type format", spec)
	}
	typeName := spec[lastSlash+1:]
	typeName = strings.TrimPrefix(typeName, "go-")
	typeName = strings.TrimSuffix(typeName, "-go")
	importPath := spec[:lastDot]
	if strings.HasPrefix(spec, "*") {
		importPath = strings.TrimPrefix(importPath, "*")
		typeName = "*" + strings.TrimPrefix(typeName, "*")
	}
	return parsedGoType{
		typeName:    typeName,
		importPath:  importPath,
		importAlias: "",
	}, nil
}

func packageNameForImport(path string) string {
	base := filepath.Base(path)
	base = strings.TrimPrefix(base, "go-")
	base = strings.TrimSuffix(base, "-go")
	if strings.HasPrefix(base, "v") && len(base) > 1 && isDigits(base[1:]) {
		parent := filepath.Base(filepath.Dir(path))
		parent = strings.TrimPrefix(parent, "go-")
		parent = strings.TrimSuffix(parent, "-go")
		return strings.Map(importPackageRune, parent)
	}
	return strings.Map(importPackageRune, base)
}

func importPackageRune(r rune) rune {
	if unicode.IsLetter(r) || unicode.IsDigit(r) || r == '_' {
		return unicode.ToLower(r)
	}
	return '_'
}

func isDigits(s string) bool {
	for _, r := range s {
		if !unicode.IsDigit(r) {
			return false
		}
	}
	return s != ""
}

func dataType(identifier *plugin.Identifier) string {
	if identifier == nil {
		return ""
	}
	if identifier.GetSchema() != "" {
		return identifier.GetSchema() + "." + identifier.GetName()
	}
	return identifier.GetName()
}
